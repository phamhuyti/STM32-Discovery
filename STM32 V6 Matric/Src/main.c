/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{ /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Configure the system clock */
  SystemClock_Config();
  /* Initialize all configured peripherals */
  MX_SPI1_Init();
  Wheel_GPIO_Init();
  MFRC522_Init();
  /* Infinite loop */
  uint8_t i = 0;
  calculator(3, 1, a);
  while (1)
  {
    switch (a[i])
    {
    case 1:
      moveForward(1);
      if (MFRC522_Check(IDCard) == MI_OK)
      {
        ID = 0;
        ID = IDCard[0];
        for (int i = 1; i < 5; i++)
        {
          ID <<= 8;
          ID += IDCard[i];
        }
      }
      else
        for (int i = 0; i < 5; i++)
          IDCard[i] = 0;
      if (ID == 0x896f0dc52e)
        i++;
      /* code */
      break;
    case 2:
      moveSidewaysRight();
      /* code */
      break;
    case 3:
      moveBackward(1);
      /* code */
      break;
    case 4:
      moveSidewaysLeft();
      /* code */
      break;

    default:
      break;
    }
  }
}
